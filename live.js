let  operation = window.prompt("Choose which operation you would like to perform                                    E.g +,-,รท,* ");
let  num1 = window.prompt("Enter First no ");
let num2 = window.prompt("Enter your second no");
operation == ( '+,-,*, รท');
num1 == parseFloat(num1);
num2 == parseFloat(num2);
if
(operation == '+' ) {
  parseInt()
   document.write(num1 + num2);
}
else if
(operation == '-' ) {
document.write(num1 - num2) 
}
else if 
(operation == '*' ) {
document.write(num1 * num2) 
}
else if 
  (operation == 'รท' ) {
document.write(num1 / num2)
  }
else {
document.write("invalid input") 
}